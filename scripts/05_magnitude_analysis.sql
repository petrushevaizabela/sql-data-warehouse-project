-- Magnitude Analysis

 -- Find total customers by countries
SELECT
	country,
	COUNT(customer_key) AS total_customers
FROM gold.dim_customers
GROUP BY country
ORDER BY total_customers DESC;

 -- Find total customers by gender
SELECT
	gender,
	COUNT(customer_key) AS total_customers
FROM gold.dim_customers
GROUP BY gender
ORDER BY total_customers DESC

 -- Find total products by category
SELECT
	category,
	COUNT(product_key) AS total_products
FROM gold.dim_products
GROUP BY category
ORDER BY total_products DESC

 -- What is the average costs in each category?
SELECT
	category,
	AVG(cost) AS average_cost
FROM gold.dim_products
GROUP BY category
ORDER BY average_cost DESC

 -- What is the total revenue generated for each category?
SELECT
	gdp.category,
	SUM(gfs.sales_amount) AS total_revenue
FROM gold.dim_products gdp
LEFT JOIN gold.fact_sales gfs
ON gdp.product_key = gfs.product_key
GROUP BY gdp.category
ORDER BY total_revenue DESC

 -- Find total revenues that is generated by each customer
SELECT
	c.customer_key,
	c.first_name,
	c.last_name,
	SUM(s.sales_amount) AS total_revenue
FROM gold.fact_sales s 
LEFT JOIN gold.dim_customers c
ON s.customer_key = c.customer_key
GROUP BY c.customer_key, first_name, last_name
ORDER BY total_revenue DESC

 -- What is the distribution of sold items across contires?
SELECT
	c.country,
	SUM(s.quantity) AS sold_items
FROM gold.fact_sales  s
LEFT JOIN gold.dim_customers c
ON s.customer_key = c.customer_key
GROUP BY c.country
ORDER BY sold_items DESC
